# 2. 排版
#   Python 有很严格的缩进排版要求
#   2.1 代码默认顶格
print('狗子')

#   2.2 同级代码, 需要保持缩进一致
#       缩进: Tab进行缩进, 4个空格
if True:
    print('二狗')
    print('狗蛋')

# 2.3 一条代码占一行
#   如果强行放在一行, 每条命令需要用 ; 隔开
# print('陈羽凡')  print('胡海泉')
print('陈羽凡'); print('胡海泉')

# 2.4 行宽
#   每条命令尽量不要超过80个字符, 如有特殊情况, 可略微超过, 但最长不得超过120
#   原因:
#       不方便在控制台查看
#       设计可能存在缺陷

