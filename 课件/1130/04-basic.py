#  	1.3 print 默认自带换行
#  	 原因: 
#  		print(内容1, 内容2, ... end="\n")
#  		\n : 代表换行
#  		
#  		如果不想换行, 手动修改end的值
print('亚洲四大邪术:', end='  ')
print('日本的化妆术', '韩国的整容术', '泰国变性术', '中国的PS术')


# 	1.4 print(
# 			内容1 \
# 			内容2 \
# 			内容3 \
# 			...
# 			内容n \
# 	 	 )

print(
		'草丛三婊:'  \
		'安其拉' 	\
		'妲己' 		\
		'王昭君' 	\
	 )


