# 4. 编码
#   最早的时候, 世界上只有128个字符, ASCII编码 (不支持中文, 其他语言)
#   支持大多数语言的编码: Unicode编码   占4B
#   优化版的编码: UTF-8   万国码
#   
#   Python3 默认编码UTF-8
# 
#   设置编码:  # -*- coding:utf-8 -*-
#       设置编码本身就是一个注释, 前面的# 不能丢
#       一般情况下, 编码写在文件的最前面的


# 小结: 
#   若无特殊情况, 文件一律使用 UTF-8 编码
#   若无特殊情况, 文件开头必须加入 #-*- coding:utf-8 -*-



