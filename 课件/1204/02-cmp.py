# 2. 比较运算符
# 	1. ==
# 	2. != 
# 	3. >
# 	4. <
# 	5. >=
# 	6. <=
#   
#   注意: 比较结果只有 True 和 False

print( 10 == 10 )
print( 10 != 10 )
print( 10 > 5 )
print( 10 < 5 )
print( 10 <= 5 )
print( 10 >= 5 )
print('~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~')

# 等价值:
print( 0 == False )
print( 0.0 == False )
print( 0+0j == False )
print('~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~')


# 不等值
print( '' == False )
print( [] == False )
print( () == False )
print( {} == False )





