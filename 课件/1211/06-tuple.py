# 5. 遍历
a = ('易筋经', '洗髓经', '金钟罩', '童子功')

i = 0
while i < len(a):
	print(i, a[i])
	i += 1

print('~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~')


for i in range(len(a)):
	print(i, a[i])

print('~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~')


for i in a:
	print(a.index(i), i)


