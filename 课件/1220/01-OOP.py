# 飞机类:
# 	飞机 就是一个类, 不代表具体的一架飞机, 只是一种描述, 不真实的
# 	对象 一个真实存在的事物, 可以看到它有什么, 它能干什么
# 
# 	图纸说明: (飞机有什么)
# 		飞机名称
# 		飞机材质
# 		...
# 		
# 		
# 	功能说明: (飞机会什么)
# 		载客
# 		飞行
# 		发射导弹
# 		...
# 		
# 	以上都只是我们想象中的飞机, 实际暂不存在
# 	想要存在, 就必须将 类 实例化为 一个真实的飞机


# 飞机对象:
# 	波音777 就是一个对象, 具体的飞机, 能将上面所描述的真实的展现出来
# 	
# 	能看到飞机上的 铭牌
# 	能看到飞机的 材质
# 	
# 	能真实看到飞机 飞行
# 	能真实看到飞机 发射导弹

# 认知:  一切皆对象

# 小结:
# 	类, 就是一个抽象, 虚拟的事物
# 	对象, 就是真实的, 实例化的事物

# 关系:
# 	类 就是对象的抽象化
# 	对象 就是类的实例化






