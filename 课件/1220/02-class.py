# 2.命名规范
#  
# 	类名
# 	------------------
# 	1) 类名采用 大驼峰命名法(每个单词的首字母大写)
# 	2) 类名一般都是 名词
# 	3) 严格区分大小写
# 	
# 	
# 	属性名
# 	-------------------
# 	1) 由数字, 字母和下划线组成
# 	2) 不能以数字开头
# 	
# 	
# 	方法名
# 	-------------------
# 	1) 动词开头
# 	
# 	
# 	属性名 和 方法名
# 	-------------------
# 	1) 一律小写, 多个单词用 下划线 隔开
# 	2) 严格区分大小写
# 	
# 	
# 	注意:
# 		类一旦定义, 就会占据一个内存
# 		类不实例化, 就不能正常使用
# 	
# 	
# 	

