# 定义 飞机类
class Plane():
	# 属性 	(类有什么)
	# 	属性就是平时的变量, 不过在类里面, 改名叫属性.  该属性专属于该类
	# ----------------------------------
	name = '波音777'
	age = 10


	# 方法  (类会什么)
	# 	方法就是平时的函数, 不过在类里面, 改名叫方法.  该方法专属于该类
	# ----------------------------------
	def fly(self):
		print('我会飞')

	def biu(self):
		print('没事射两下')



